function addActive(target) { //active Class 추가
    target.addClass("active");
}

function removeActive(target) { //active Class 제거
    target.removeClass("active");
}

function toggleActive(target) { //active Class 추가 및 제거
    target.toggleClass("active");
}

function addHide(target) { //hide Class 추가
    target.addClass("hide");
}

function removeHide(target) { //hide Class 제거
    target.removeClass("hide");
}